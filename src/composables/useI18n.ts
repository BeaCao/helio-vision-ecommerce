const currency = ref('EUR');

const exchangeRates: Record<string, number> = {
  EUR: 1,
  USD: 1.08,
  GBP: 0.85,
  JPY: 165.50
};

export function useI18n() {
  const t = computed(() => {
    return (key: string) => {
      return messages[locale.value]?.[key] || messages['es'][key] || key;
    };
  });

  const setLocale = (newLocale: string) => {
    if (messages[newLocale]) {
      locale.value = newLocale;
    }
  };

  const setCurrency = (newCurrency: string) => {
    if (exchangeRates[newCurrency]) {
      currency.value = newCurrency;
    }
  };

  const formatPrice = computed(() => {
    return (priceInEur: number) => {
      const converted = priceInEur * exchangeRates[currency.value];
      if (currency.value === 'JPY') return new Intl.NumberFormat('ja-JP', { style: 'currency', currency: 'JPY', maximumFractionDigits: 0 }).format(converted);
      if (currency.value === 'USD') return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', maximumFractionDigits: 0 }).format(converted);
      if (currency.value === 'GBP') return new Intl.NumberFormat('en-GB', { style: 'currency', currency: 'GBP', maximumFractionDigits: 0 }).format(converted);
      return new Intl.NumberFormat('es-ES', { style: 'currency', currency: 'EUR', maximumFractionDigits: 0 }).format(converted);
    };
  });

  return {
    t,
    locale,
    setLocale,
    currency,
    setCurrency,
    formatPrice
  };
}
